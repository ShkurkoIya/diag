/*
 * asn1c_lte_decode.c — clean C adapter for LTE RRC PDU decoding.
 *
 * Replaces main4g.c from the reference project. The reference's parse4G()
 * prints decoded contents to FILE* — useful for their CLI debugging tool,
 * but we need structured access from C++ to extract MCC/MNC/TAC/CID etc.
 *
 * This file does NOT have a main() — it's pure library code.
 *
 * USAGE FROM C++ (asn1c_lte_bridge.cpp):
 *
 *   extern "C" {
 *       #include "asn1c_lte_decode.h"
 *   }
 *
 *   decoded_pdu_t pdu = decode_pdu_by_name(
 *       "BCCH-DL-SCH-Message", data, size);
 *
 *   if (pdu.success) {
 *       BCCH_DL_SCH_Message_t* msg = (BCCH_DL_SCH_Message_t*)pdu.structure;
 *       // ... walk msg fields ...
 *       free_pdu(&pdu);
 *   }
 *
 * INTEGRATION:
 *   Place this file in src/asn1c_lte/ (next to all the asn1c-generated .c).
 *   CMake's file(GLOB) picks it up automatically.
 *   No CMake changes needed.
 */

#include <stdio.h>
#include <string.h>
#include <stdlib.h>

#include "asn_application.h"
#include "asn_internal.h"

/* asn_pdu_collection[] is generated by `asn1c -pdu=all` and lives in
 * pdu_collection.c. It's a NULL-terminated array of all PDU types. */
#ifdef ASN_PDU_COLLECTION
extern asn_TYPE_descriptor_t *asn_pdu_collection[];
#else
#error "ASN_PDU_COLLECTION must be defined — set in CMake target_compile_definitions"
#endif

#include "asn1c_lte_decode.h"

/* ------------------------------------------------------------------ */
/* Decode a PDU by name using UNALIGNED PER (UPER) — what LTE/UMTS use */
/* ------------------------------------------------------------------ */
decoded_pdu_t decode_pdu_by_name(const char *pdu_name,
                                 const uint8_t *data,
                                 size_t size)
{
    decoded_pdu_t result;
    memset(&result, 0, sizeof(result));

    if (!pdu_name || !data || size == 0) {
        result.error = "invalid arguments";
        return result;
    }

    /* Find the PDU type descriptor by name */
    asn_TYPE_descriptor_t *pdu_type = NULL;
    for (int i = 0; asn_pdu_collection[i] != NULL; i++) {
        if (strcmp(asn_pdu_collection[i]->name, pdu_name) == 0) {
            pdu_type = asn_pdu_collection[i];
            break;
        }
    }
    if (!pdu_type) {
        result.error = "unknown PDU type name";
        return result;
    }

    /* Decode using UPER (Unaligned PER) — the wire format LTE RRC uses */
    void *structure = NULL;
    asn_dec_rval_t rval = asn_decode(NULL,
                                     ATS_UNALIGNED_BASIC_PER,
                                     pdu_type,
                                     &structure,
                                     data,
                                     size);

    switch (rval.code) {
        case RC_OK:
            result.structure = structure;
            result.type      = pdu_type;
            result.success   = 1;
            result.consumed  = (size_t)rval.consumed;
            break;
        case RC_WMORE:
            result.error = "more data needed (truncated input)";
            if (structure) ASN_STRUCT_FREE(*pdu_type, structure);
            break;
        case RC_FAIL:
        default:
            result.error = "decode failed";
            if (structure) ASN_STRUCT_FREE(*pdu_type, structure);
            break;
    }

    return result;
}

/* ------------------------------------------------------------------ */
/* Free a decoded structure                                            */
/* ------------------------------------------------------------------ */
void free_pdu(decoded_pdu_t *pdu)
{
    if (pdu && pdu->structure && pdu->type) {
        ASN_STRUCT_FREE(*pdu->type, pdu->structure);
        pdu->structure = NULL;
        pdu->type      = NULL;
        pdu->success   = 0;
    }
}

/* ------------------------------------------------------------------ */
/* Print decoded PDU to a FILE — for debugging, mirrors parse4G()      */
/* ------------------------------------------------------------------ */
void print_pdu(const decoded_pdu_t *pdu, FILE *out)
{
    if (!pdu || !pdu->success || !out) return;
    asn_fprint(out, pdu->type, pdu->structure);
}

/* ------------------------------------------------------------------ */
/* Walk asn_pdu_collection and print all known PDU type names          */
/* (for sanity-checking that the right collection is linked)           */
/* ------------------------------------------------------------------ */
int list_pdu_types(FILE *out)
{
    int count = 0;
    if (!out) out = stderr;
    for (int i = 0; asn_pdu_collection[i] != NULL; i++) {
        fprintf(out, "  [%3d] %s\n", i, asn_pdu_collection[i]->name);
        count++;
    }
    return count;
}
