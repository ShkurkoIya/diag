#error `asn1c -pdu=...` adds necessary flags automatically.
#endif
#endif


int parse3G(uint8_t *packet, size_t size, const char *pduName,FILE *fp)
{
    FILE *binary_out;
    asn_TYPE_descriptor_t *pduType = PDU_Type_Ptr;
    asn_TYPE_descriptor_t *anyPduType = PDU_Type_Ptr;
    asn_TYPE_descriptor_t result = {0};
    ssize_t suggested_bufsize = 8192; /* close or equal to stdio buffer */
    int number_of_iterations = 1;
    int num;
    int ch;
    enum asn_transfer_syntax isyntax = ATS_UNALIGNED_BASIC_PER;
    enum asn_transfer_syntax osyntax = ATS_NONSTANDARD_PLAINTEXT;

    void *structure = 0; /* Decoded structure */
    setvbuf(fp, 0, _IOLBF, 0);

    uint8_t *i_bptr = packet;
    size_t i_size = size;
    printf("\nstart decode,size %ld, pduname %s\n", i_size, pduName);

    for(int i=0;;i++){
        pduType = asn_pdu_collection[i];
        if (pduType == 0){
            printf("not found %s\n",pduName);
            result.name = "not_found";
            return 1;
        }
        if (strcmp(pduType->name, pduName) == 0){
            break;
        }
    }
    printf("info pduType: %s\n   ", pduType->name);
    asn_dec_rval_t rval = asn_decode(0, isyntax, pduType, (void **)&structure,
                          i_bptr, i_size);
    setvbuf(fp, 0, _IOLBF, 0);
    // 0x00 0x80 0x5c 0x31 0x18 0x6f 0xf9 0x23 0xa4 0x36 0x07 0x96 0x60 0x10 0x00 0x0c 0x04 0x10 0xee 0x31 0x6a 0xa0 0x54 0x06 0x90 0x06 0x01 0x4d 0xcc 0x2c 0x18 0x22 0x4a 0x3a 0xe4 0x7d 0x93 0xc0 0x00 0x00 0x00
    switch (rval.code)
    {
        case RC_OK:
            asn_fprint(fp, pduType, structure);
//            xer_fprint(stdout, pduType, structure);
        printf("RC_OK\n");
        break;
        case RC_WMORE:
            printf("RC_WMORE\n");
        break;
        case RC_FAIL:
            printf("RC_FAIL\n");
        break;
        default:
            break;
    }
    ASN_STRUCT_FREE(*pduType, structure);
    printf("end decode\n");

    return 0;
}
